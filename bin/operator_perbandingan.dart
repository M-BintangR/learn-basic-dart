/**
 *  (==  : Sama Dengan)
 *  (!=  : Tidak Sama Dengan)
 *  (>   : Lebih Dari)
 *  (<   : Kurang Dari)
 *  (>=  : Lebih Besar Atau Sama Dengan)
 *  (<=  : Lebih Kecil Atau Sama Dengan)
 */

void main() {
  print(10 == 10); // true
  print(10 != 10); // false
  print(10 > 10); // false
  print(10 >= 10); // true
  print(10 < 10); // false
  print(10 <= 10); // true

  print('Bintang' == 'Bintang'); // true
  print('Bintang' != 'Bintang'); // false
}
