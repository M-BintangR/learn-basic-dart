/**
 *  (+ => Pertambahan)
 *  (-, -expresion => Pengurangan atau negatif)
 *  (* => Perkalian)
 *  (/ => Pembagian hasil double)
 *  (~/ => Pembagian hasil int)
 *  (% => Sisa bagi)
 * 
 */

void main() {
  var first = 10;
  var second = 3;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}
